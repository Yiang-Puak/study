# ============================================================
# 1. 什么是变量？
# ============================================================

# 变量就是一个"名字标签"，用来记住某个值
# 类比：你在水杯上贴一张标签写着"我的杯子"
#      以后你说"给我我的杯子"，别人就知道拿哪个

# 在 Python 中，创建变量非常简单：
#   变量名 = 值

name = "张三"
age = 20

print("--- 变量的基本概念 ---")
print(f"name = {name}")    # 输出：name = 张三
print(f"age = {age}")     # 输出：age = 20

# 变量可以重新赋值（换标签贴到别的东西上）
name = "李四"
print(f"name 改了之后 = {name}")  # 输出：name 改了之后 = 李四


# ============================================================
# 2. 变量命名规则
# ============================================================

print("\n--- 变量命名规则 ---")

# 规则1：只能包含字母、数字、下划线
#   合法：my_name, name2, _private
#   不合法：2name（不能以数字开头）, my-name（不能含减号）, my name（不能有空格）

# 规则2：区分大小写
Name = "大N"
name = "小n"
print(f"Name = {Name}, name = {name}")  # 这是两个不同的变量！

# 规则3：不能用 Python 保留字（关键字）
#   不合法：if = 1, for = 2, class = 3
#   这些词 Python 自己要用，你不能抢

# 命名建议（不是规则，但面试和工作中很重要）：
#   - 用有意义的英文：user_name 比 x 好
#   - 多个单词用下划线连接：student_score（蛇形命名法）
#   - 常量全大写：MAX_SIZE = 100

student_score = 85
MAX_SIZE = 100
print(f"学生分数 = {student_score}")
print(f"最大尺寸 = {MAX_SIZE}")


# ============================================================
# 3. 基本数据类型
# ============================================================

print("\n--- 基本数据类型 ---")

# --- 3.1 整数 int ---
# 整数就是数学里的整数：... -2, -1, 0, 1, 2, 3 ...
count = 10
negative = -5
print(f"整数 count = {count}, 类型是 {type(count)}")
# type() 函数可以查看一个值的类型

# --- 3.2 浮点数 float ---
# 浮点数就是"小数"，因为小数点可以"浮动"
price = 19.99
temperature = -3.5
print(f"浮点数 price = {price}, 类型是 {type(price)}")

# --- 3.3 字符串 str ---
# 字符串就是"一串字符"，用单引号或双引号包裹
# 类比：一串项链，上面串了很多珠子（字符）
greeting = "你好"
goodbye = '再见'
print(f"字符串 greeting = {greeting}, 类型是 {type(greeting)}")

# 字符串可以用 + 拼接
full_greeting = greeting + "，世界"
print(f"拼接后 = {full_greeting}")

# 字符串可以用 * 重复
repeat = "哈" * 3
print(f"重复 = {repeat}")  # 哈哈哈

# --- 3.4 布尔值 bool ---
# 布尔值只有两个：True（真）和 False（假）
# 类比：开关，只有开和关两种状态
is_student = True
is_raining = False
print(f"布尔值 is_student = {is_student}, 类型是 {type(is_student)}")

# 布尔值常用于条件判断
print(f"5 > 3 的结果是 {5 > 3}")   # True
print(f"5 < 3 的结果是 {5 < 3}")   # False

# --- 3.5 空值 None ---
# None 表示"什么都没有"、"空"、"不存在"
# 类比：一个空盒子
result = None
print(f"空值 result = {result}, 类型是 {type(result)}")


# ============================================================
# 4. 类型转换
# ============================================================

print("\n--- 类型转换 ---")

# 有时候需要把一个类型变成另一个类型

# int()：转成整数（小数部分直接丢弃，不是四舍五入）
print(f"int(3.7) = {int(3.7)}")    # 3
print(f"int('42') = {int('42')}")  # 42（字符串转整数）

# float()：转成浮点数
print(f"float(3) = {float(3)}")      # 3.0
print(f"float('3.14') = {float('3.14')}")  # 3.14

# str()：转成字符串
print(f"str(42) = {str(42)}")        # "42"
print(f"str(3.14) = {str(3.14)}")    # "3.14"

# bool()：转成布尔值
print(f"bool(1) = {bool(1)}")        # True
print(f"bool(0) = {bool(0)}")        # False（0 是 False）
print(f"bool('') = {bool('')}")      # False（空字符串是 False）
print(f"bool('a') = {bool('a')}")    # True（非空字符串是 True）


# ============================================================
# 5. 查看变量类型
# ============================================================

print("\n--- type() 函数查看类型 ---")

x = 10
